STK SEGMENT PARA STACK 'ST_'
	DW 20 DUP(?)
STK ENDS

DSG SEGMENT PARA 'DS_'
	DIZI DB 1,2,3,4,5,7,7,9,11,13, 490 DUP(0)
	TEK DW 0
	CIFT DW 0
	NOFELEM DW 500
DSG ENDS

CSG SEGMENT PARA 'CS_'
	ASSUME CS: CSG, DS:DSG, SS:STK
	ANA PROC FAR
		PUSH DS	
		XOR AX, AX
		PUSH AX
		MOV AX, DSG
		MOV DS, AX
		
		MOV CX, NOFELEM
		XOR SI, SI

		L1:
			TEST DIZI[SI], 01H
			JZ CIFT_LBL
			INC TEK
			CIFT_LBL:
			INC SI 
		LOOP L1
		MOV AX, NOFELEM
		SUB AX, TEK
		MOV CIFT, AX

		RETF
	ANA ENDP
CSG ENDS
END ANA
